const FIRST_EMOJI_CODE = 127397
export const getFlagEmoji = (language: string) => {
  return (
    languageFlags[language]
    ?? language
      .toUpperCase()
      .replace(/./g, char =>
        String.fromCodePoint(FIRST_EMOJI_CODE + char.charCodeAt(0)),
      )
  )
}
